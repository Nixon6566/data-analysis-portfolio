#MULTI-DIMENSIONAL LISTS
'''Another way to create a multidimensional List is by creating a second
List separately and then adding the name of that List to your original list
(in the same way that we added Variables to our List earlier):'''

numbers = [1, 2, 3, 4]
names = ["John", "Mary", "Beatrice", "Bluto", numbers]
print(names [4] [2])
